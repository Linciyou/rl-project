# 2. 建立 PINN (物理驅動神經網路) 模型
# ==========================================
class UPW_PINN_LSTM(nn.Module):
    def __init__(self, input_dim, output_dim, hidden_dim=64):
        super(UPW_PINN_LSTM, self).__init__()
        self.lstm = nn.LSTM(input_size=input_dim, hidden_size=hidden_dim, batch_first=True)
        self.fc = nn.Linear(hidden_dim, output_dim)

    def forward(self, x, hidden=None):
        out, hidden = self.lstm(x, hidden)
        delta_state = self.fc(out[:, -1, :])
        current_state = x[:, -1, :-1]
        next_state = current_state + delta_state
        return next_state, delta_state, hidden

def train_pinn_twin(X, Y, state_dim, action_dim, toc_idx, epochs=250):
    print(">>> [2/4] 訓練 PINN 數位分身模型 (融合數據與物理法則)...")
    model = UPW_PINN_LSTM(input_dim=state_dim + action_dim, output_dim=state_dim)
    optimizer = optim.Adam(model.parameters(), lr=0.003)
    mse_criterion = nn.MSELoss()

    for epoch in range(epochs):
        model.train()
        optimizer.zero_grad()

        next_state_pred, delta_state_pred, _ = model(X)
        actions = X[:, 0, -1]
        delta_toc = delta_state_pred[:, toc_idx]

        # ------------------------------------------------
        # 🌟 PINN 核心：加上「物理速限」的混合損失函數
        # ------------------------------------------------
        loss_data = mse_criterion(next_state_pred, Y)

        # 規則 A1: 若沒保養 (action=0)，TOC 絕對不能減少
        penalty_no_maint_min = torch.clamp(-delta_toc, min=0.0) * (1 - actions)

        # 🚀 [新增防暴衝機制] 規則 A2: 若沒保養，TOC 每週最多只能增加 0.4 ppb
        penalty_no_maint_max = torch.clamp(delta_toc - 0.4, min=0.0) * (1 - actions)

        # 規則 B: 若保養 (action=1)，TOC 必須大幅下降 (至少降 1.5 ppb)
        penalty_maint = torch.clamp(delta_toc + 1.5, min=0.0) * actions

        # 給予物理法則極高權重 (10.0)，確保網路不會因為 historical data 偏高而暴衝
        loss_physics = 10.0 * torch.mean(penalty_no_maint_min + penalty_no_maint_max + penalty_maint)

        total_loss = loss_data + loss_physics
        total_loss.backward()
        optimizer.step()

        if (epoch+1) % 50 == 0:
            print(f"    PINN Epoch [{epoch+1:3d}/{epochs}] | 總Loss: {total_loss.item():.4f} (Physics: {loss_physics.item():.4f})")

    return model

