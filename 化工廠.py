# 3. 強化學習模擬環境 (PINN 驅動)
# ==========================================
class PINNSimulatedUPWEnv(gym.Env):
    def __init__(self, pinn_model, initial_states, scaler, feature_names):
        super(PINNSimulatedUPWEnv, self).__init__()
        self.pinn_model = pinn_model
        self.pinn_model.eval()
        self.initial_states = initial_states
        self.scaler = scaler
        self.feature_names = feature_names

        self.state_dim = len(feature_names)
        self.action_space = spaces.Discrete(2)
        self.observation_space = spaces.Box(low=-10.0, high=10.0, shape=(self.state_dim,), dtype=np.float32)

        self.toc_idx = feature_names.index('TOC_ppb')
        self.step_count = 0
        self.max_steps = 52
        self.consecutive_no_maintenance = 0

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.step_count = 0
        self.consecutive_no_maintenance = 0

        idx = np.random.randint(0, len(self.initial_states))
        self.current_state = self.initial_states[idx].astype(np.float32)

        real_state = self.scaler.inverse_transform([self.current_state])[0]
        real_state[self.toc_idx] = 2.0  # 健康起點
        self.current_state = self.scaler.transform([real_state])[0].astype(np.float32)

        self.hidden_state = (
            torch.zeros(1, 1, self.pinn_model.lstm.hidden_size),
            torch.zeros(1, 1, self.pinn_model.lstm.hidden_size)
        )
        return self.current_state, {}

    def step(self, action):
        self.step_count += 1

        action_arr = np.array([action], dtype=np.float32)
        pinn_input = np.concatenate([self.current_state, action_arr])
        pinn_input_tensor = torch.FloatTensor(pinn_input).view(1, 1, -1)

        with torch.no_grad():
            next_state_tensor, _, self.hidden_state = self.pinn_model(pinn_input_tensor, self.hidden_state)

        self.current_state = next_state_tensor.numpy().flatten()
        real_state = self.scaler.inverse_transform([self.current_state])[0]
        real_toc = real_state[self.toc_idx]

        # 避免保養後數值無限制變負的底線保護
        if real_toc < 1.0: real_toc = 1.0
        real_state[self.toc_idx] = real_toc
        self.current_state = self.scaler.transform([real_state])[0].astype(np.float32)

        weeks_without_maint = self.consecutive_no_maintenance
        if action == 0:
            self.consecutive_no_maintenance += 1
        else:
            self.consecutive_no_maintenance = 0

        # --- 獎勵函數 ---
        reward = 1.0
        done = False

        if real_toc > 5.0:
            reward -= 100
            done = True
        elif real_toc > 4.0:
            reward -= 10  # 這一次 AI 會有充分的時間停留在 4.x 收到警告！

        if action == 1:
            reward -= 15

        if self.step_count >= self.max_steps:
            done = True

        info = {'real_toc': real_toc, 'no_maint_weeks': weeks_without_maint}
        return self.current_state, float(reward), done, False, info

