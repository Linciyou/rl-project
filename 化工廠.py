# ==========================================
# 1. 資料載入與前處理
# ==========================================
def load_and_preprocess_data(csv_path):
    print(">>> [1/4] 載入並處理歷史資料...")
    df = pd.read_csv(csv_path)
    df = df[df['arm'] == 'Legacy_2passRO_MB'].reset_index(drop=True)

    features = [
        'feed_SDI', 'feed_conductivity_uScm', 'resistivity_MOhm_cm',
        'TOC_ppb', 'cation_leak_ppb', 'anion_leak_ppb', 'deltaP_bar'
    ]

    df['action'] = df['MB_regen'].apply(lambda x: 1.0 if x == 'Yes' else 0.0)
    df[features] = df[features].fillna(method='ffill').fillna(0)

    scaler = StandardScaler()
    scaled_states = scaler.fit_transform(df[features])
    actions = df['action'].values.reshape(-1, 1)

    X, Y = [], []
    for i in range(len(scaled_states) - 1):
        x_combined = np.concatenate([scaled_states[i], actions[i]])
        X.append(x_combined)
        Y.append(scaled_states[i+1])

    X = torch.FloatTensor(np.array(X)).unsqueeze(1)
    Y = torch.FloatTensor(np.array(Y))

    return X, Y, scaled_states, scaler, features

